package src.basic3_16Dec;

public class Lab39 {
    public static void main(String[] args) {
        //Increment Operator
        //Pre and Post
        int a=10;
      //  a= a +1;
      //  a+=1;
        a++ ; //Post Increment ->a=a+1
        ++a; // Pre Increment  = a = a+1;

        System.out.println(a);
    }
}

